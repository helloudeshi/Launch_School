=begin   
Create a method that takes a string argument and 
returns the character that occurs most often in the 
string. If there are multiple characters with the 
same greatest frequency, return the one that 
appears first in the string. When counting characters, 
consider uppercase and lowercase versions to be the same.

p most_common_char('Hello World') == 'l'
p most_common_char('Mississippi') == 'i'
p most_common_char('Happy birthday!') == 'h'
p most_common_char('aaaaaAAAA') == 'a'

my_str = 'Peter Piper picked a peck of pickled peppers.'
p most_common_char(my_str) == 'p'

my_str = 'Peter Piper repicked a peck of repickled peppers. He did!'
p most_common_char(my_str) == 'e'
=end

def most_common_char(string)
  char_hash = string.downcase.chars.tally
  max_count = char_hash.values.max
  char_hash.key(max_count) 
end

p most_common_char('Hello World') == 'l'
p most_common_char('Mississippi') == 'i'
p most_common_char('Happy birthday!') == 'h'
p most_common_char('aaaaaAAAA') == 'a'

my_str = 'Peter Piper picked a peck of pickled peppers.'
p most_common_char(my_str) == 'p'

my_str = 'Peter Piper repicked a peck of repickled peppers. He did!'
p most_common_char(my_str) == 'e'